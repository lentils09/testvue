<template>
    <div class="todo-item" v-bind:class="{'is-complete':todo.completed}"> <!-- Binding class on condition that todo.completed being true -->
        <p>
            <input type="checkbox" v-on:change="markComplete"> <!-- Create checkboxes on side of todos and binds an event -->
            {{ todo.title }}
            <button @click="$emit('del-todo', todo.id)" class="del">x</button>
        </p>
    </div>
</template>

<script>
export default {
    name: "TodoItem",
    props: ["todo"],
    methods: {
        markComplete() { // function to change todo.completed to its opposite event from line 4
            this.todo.completed = !this.todo.completed;
        }
    }
}
</script>

<style scoped>
    .todo-item {
        background: #f4f4f4;
        padding: 10px;
        border-bottom: 1px #ccc dotted;
    }

    .is-complete {
        text-decoration: line-through;
    }

    .del {
        background: #ff0000;
        color: #fff;
        border: none;
        padding: 5px 9px;
        border-radius: 50%;
        cursor: pointer;
        float: right;
    }
</style>
